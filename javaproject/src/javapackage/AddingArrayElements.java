package javapackage;
public class AddingArrayElements {

	public static void main(String[] args) {
		int sum=0;
		int[] arr= {21,54,58,45,65,52,55};
		for(int i=0;i<arr.length;i++) {
		System.out.println("the array elements:"+arr[i]);
		sum=sum+arr[i];
		}
		System.out.println("the Sum of the array elements:"+sum);
		
		}
		
		
		
	}


