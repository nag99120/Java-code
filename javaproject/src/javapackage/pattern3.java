package javapackage;

public class pattern3 {

	public static void main(String[] args) {
		int i ,j;
		for(i=0;i<=5;i++)
		{
			for (j=0;j<=i;j++)
			{
				System.out.print("*");
			}
			 System.out.println("");

		}
		int k ,l;
		for(k=0;k<=4;k++)
		{
			for (l=k;l<=4;l++)
			{
				System.out.print("*");
			}
			 System.out.println("");

		}
	}

}
